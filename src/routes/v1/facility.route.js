const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const facilityValidation = require('../../validations/facility.validation');
const facilityController = require('../../controllers/facility.controller');
const router = express.Router();
const multer = require('multer');
const path = require('path');
const fs = require('fs');
const dirPath = 'src/public/assets/images/facilities';

const storage = multer.diskStorage({
  destination: function (req, file, callback) {
    if (!fs.existsSync(dirPath)){
      fs.mkdirSync(dirPath, { recursive: true });
    }
    callback(null, dirPath);
  },
  filename: function (req, file, cb) {
    cb(
      null,
      new Date().valueOf() +
      '_' +
      file.originalname
    );
  }
});

function checkFileType(file, cb){
  // Allowed ext
  const filetypes = /jpeg|jpg|png|gif/;
  // Check ext
  const extname = filetypes.test(path.extname(file.originalname).toLowerCase());
  // Check mime
  const mimetype = filetypes.test(file.mimetype);

  if(mimetype && extname){
    return cb(null,true);
  } else {
    cb(new Error('Error: Images Only [ jpeg|jpg|png|gif ]!'));
  }
}

const upload = multer({
  storage : storage,
  limits: { fileSize: 2000000 },
  fileFilter: function(_req, file, cb){
  checkFileType(file, cb);
}
}).single('image');

router
  .route('/')
  .post(auth('manageFacilities'),upload, validate(facilityValidation.createFacility), facilityController.createFacility)
  .get(auth('getFacilities'), validate(facilityValidation.getFacility), facilityController.getFacilities)

router
  .route('/search')
  .post(auth('getFacilities'), validate(facilityValidation.getSearchFacility), facilityController.getSearchFacility);

router
  .route('/image/:facilityId')
  .get(auth('getFacilities'), validate(facilityValidation.getImageFacility), facilityController.getImageFacility);

router
  .route('/:facilityId')
  .get(auth('getFacilities'), validate(facilityValidation.getFacility), facilityController.getFacility)
  .put(auth('manageFacilities'),upload, validate(facilityValidation.updateFacility), facilityController.updateFacility)
  .delete(auth('manageFacilities'), validate(facilityValidation.deleteFacility), facilityController.deleteFacility);

module.exports = router;

